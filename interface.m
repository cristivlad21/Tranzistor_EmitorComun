title('Vi(t) - tensiunea de intrare');
grid on;
hold on;
xlabel('Timp [s]');
ylabel('Amplitudine [V]');

%-----------semnalul de iesire
vO=Av.*A.*sin(2.*pi.*f.*t);             %-----------am scos componenta continua
for i = 1:length(vO)
    if vO(i) > VAl
        vO(i)  = VAl;
    elseif vO(i)<-VAl
        vO(i) = -VAl;
    end
end
subplot(224);
plot(t,vO,'k','Linewidth', 2);
title('Vo(t) - tensiunea de iesire');
